static unsigned char batteryGetPowerPer(BatteryInfo_t *table, unsigned short adc)
{
    unsigned char i;
    if(adc <= table[0].adc) return table[0].per;
    if(adc >= table[BATTERY_DATA_COUNT - 1].adc) return table[BATTERY_DATA_COUNT - 1].per;
    for(i = 0; i < BATTERY_DATA_COUNT - 1; i++) {
        if((adc > table[i].adc) && (adc <= table[i + 1].adc)) return table[i].per;
    }
    return 100;
}

unsigned short adcSum = 0, adcBuff[16] = {0}, adcIdx = 0;

void batteryInit(Battery_t *bat, BatteryGetAdcFunc getAdc, BatteryChargeStateFunc getChargeState)
{
    unsigned char i;
	bat->getAdc = getAdc;
    bat->getChargeState = getChargeState;
    bat->en = 1;
    for(i = 0; i < 16; i++) {
        adcBuff[i] = bat->getAdc();
        adcSum += adcBuff[i];
    }
    bat->adc = adcSum >> 4;
    bat->per = batteryGetPowerPer(batSupply, bat->adc);
}

void batteryProcess(Battery_t *bat)
{
    if(bat->en) {
        adcSum -= adcBuff[adcIdx];
        adcBuff[adcIdx] = bat->getAdc();
        adcSum += adcBuff[adcIdx];
        bat->adc = adcSum >> 4;
        if(++adcIdx == 16) adcIdx = 0;
        bat->per = batteryGetPowerPer(batSupply, bat->adc);
    }
}
